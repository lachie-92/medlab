                        2004 to 2010, he was founding president of the Natural Health Care Alliance, a national body
                        representing the Natural Healthcare / Complementary Medicine profession.
                    </p>
                    <p style="text-align: justify;">
                        Mark and other medical colleagues experienced in integrative medicine have established the
                        Centre for Evidence-Based Complementary Medicine, a private venture committed to building
                        the evidence-base for natural, complementary and integrative medicine approaches to
                        prevention and treatment of disease. Mark's goal is to extract the best and most clinically
                        relevant complementary medicine research to support integrative medicine practitioners and
                        improve health outcomes for all Australians.
