                <div class="panel-body medlab_panel_content">
                    <h3 style="text-align: center">
                        <strong>Vitamin D – Your Best Defence Against The Flu</strong>
                    </h3>
                    <h5 style="text-align: center">
                        <strong>Margaret Jasinska</strong><br>
                    </h5>
                    <br>
                    <p style="text-align: justify;">
                        This winter thousands of Australians will come down with influenza, otherwise known as the flu. By ensuring you have optimal
                        blood vitamin D levels, you are setting yourself up for the best defence from becoming infected.
                    </p>
                    <p style="text-align: justify;">
                        The flu is caused by a specific group of RNA viruses and can be spread by an infected person coughing or sneezing.
                        You can also catch the flu from touching an item contaminated by the virus.
                    </p>

                    <br>
                    <div class = "container">
                        <div class="col-md-2"></div>
                    <div class="col-md-4">
                    <p style="text-align: justify;">
