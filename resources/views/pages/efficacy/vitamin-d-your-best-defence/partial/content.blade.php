                        </div>

                    <div class="col-md-4">
                    <em><br><br>Did you know that the flu virus can survive for more than eight hours on hard surfaces like plastic or stainless steel, and more than an hour in the air of a closed room?</em>
                    </p>
                    </div>

                        <div class="col-md-2"></div>
                        </div>


                    <div class = "container">

                        <div class="col-md-2"></div>

                        <div class="col-md-4">


                            <em><br><br>You can see how incredibly easy it is to pick up an infection if you touch money, door handles, or use public transport!</em>
                            </p>
                        </div>

                        <div class="col-md-4">
                        <p style="text-align: justify;">
                            <img class="img-responsive" alt="Human Anatomy" width="320px;" src="/img/efficacy/vitamin-d/Picture2.png" title="Human Anatomy" >

                            </div>
                        <div class="col-md-2"></div>
                    </div>

                    <br><br>

                    <p style="text-align: justify;">
                        There is a big difference between a cold and the flu.
                    </p>

                    <p style="text-align: justify;">
                        A cold is much less severe. The flu tends to come on suddenly and can make you feel like you’ve been hit by a bus.
                        It causes a high fever and there is usually loss of appetite and coughing. The flu leaves you flat on your back,
                        unable to work or study.
                    </p>

                    <p style="text-align: justify;">
                        You may not realise that Influenza causes 1,500 to 3,500 deaths in Australia each year.
                        A new flu vaccine is developed annually, however, because the viruses responsible mutate so quickly,
                        the vaccine is often not effective.<br><br>
                    </p>

                    <p style="text-align: justify;">
                        <h4>Vitamin D is a powerful ally in helping to keep you flu-free this winter.</h4>

                    </p>

                    <p style="text-align: justify;">
                        <em>You probably know that vitamin D is critical for keeping your bones strong, but did you know it benefits your immune system?</em>
                    </p>
                    <br><br>


                    <div class = "container">

                        <div class="col-md-2"></div>

                        <div class="col-md-4">
                            <img class="img-responsive" alt="Human Anatomy" width="320px;" src="/img/efficacy/vitamin-d/Picture3.png" title="Human Anatomy" >


                        </div>

                        <div class="col-md-4">
                            <p style="text-align: justify;">
                            <p>Your white blood cells contain vitamin D receptors. When vitamin D binds to them, it causes the release of antimicrobial proteins that help your body fight invading viruses and germs.</p>
                            <p>People with lower blood vitamin D levels are more susceptible to getting sick, and tend to have a more severe infection.</p>


                        </div>
                        <div class="col-md-2"></div>
                    </div><br><br>


                    <p style="text-align: justify;">
                    <h4>A study conducted on healthy adults found that people with lower levels of vitamin D
                        were twice as likely to get the flu, compared to people with high levels.</h4>

                    </p>


                    <p style="text-align: justify;">
                        Vitamin D insufficiency is surprisingly common in sunny Australia, particularly in winter. You make vitamin D in your skin when you are
                        exposed to the sun’s UVB rays. Midday is when UVB rays are strongest, and this is the time when many people are indoors working.
                    </p>
                    <p style="text-align: justify;">
                        In much of Australia the sun is too weak during winter, therefore even if you are outdoors a lot, you could still be low in vitamin D.
                        A vitamin D supplement may be your best option for keeping your immune system strong.

                    </p>
