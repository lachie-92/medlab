                    <p>
                        Depression is a common presenting problem in both primary and specialist health care settings. Both health and allied health
                        clinicians are well aware of what the research clearly states about response rates. Approximately 40-50% of all people with
                        major depression respond well to standard treatment, being either medication or psychological treatment. However, the same
                        is not the case for the remainder of this group who present with increasing complexity in clinical presentation and a mixed
                        treatment response. While depression is marked by changes in mood and capacity to function, it is more than a disorder of
                        the way we feel. Approximately 30% of the mixed response group would fall into the treatment resistant depression (TRD)
                        category, which means that they remain depressed despite best standard treatment being administered. While there are
                        different views regarding diagnostic classification of what might be considered TRD, some key patient history will aid
                        identification of these patients. Firstly is the number of previous depressive episodes and remissions. By the time a
                        patient has experienced three diagnosed episodes of major depression they have in excess of 90% probability of further
                        episodes and these patients tend to relapse quickly if they have any positive treatment response. Typically, there is a
                        history of poor response to different classes of medications. The patient presents as meeting the criteria for major
                        depressive episodes (severe) while on an anti-depressant medication, which is doing little to alleviate symptoms. There
                        is anecdotal evidence that suicide risk for this group may be modestly higher than non-TRD. While this issue requires
                        further research attention, it is prudent to regard risk assessment as a priority for this group.
                    </p>

                    <p>

                        Inflammatory based disorders, endocrine, lipedema, insulin resistance and diabetes, and elevated blood pressure is also
                        common in TRD patients. These patients often complain of poor concentration and memory, low energy levels and other
                        symptoms that are not sufficiently explained by the symptoms of depression. While some of these symptoms in previous
                        times would have been attributed to depressive symptomology, especially neurovegetative symptoms, they may be better
                        understood as part of a key imbalance of neurophysiology in depression that may be especially common in TRD.

                    </p>

                    <hr>
                    <h3>Probiotics as an adjuvant treatment</h3>

                    <img class="img-responsive" height="250" width="200" style="display: block; margin-left: auto; margin-right: auto; float: right;" alt="Figure 1" src="/img/efficacy/novel-adjunct-for-depression/shuterstocknrg.jpg" title="Figure 1">


                    <p>

                        You are probably noticing the developing theme of inflammation and GI factors as we did in our pilot work. Both SAMe and
                        orotate mitigate inflammation in addition to exerting independent effects. The question remains, where does the inflammation
                        originate and what causes it? There is reasonably robust evidence that dysbiosis is an important cause of systemic
                        inflammation in the human body and immune dysregulation. The bacteria lining the GIT have important functions maintaining
                        gap junctions and GIT receptor signalling through secondary mechanisms. What is less well known is that these bacteria make
                        all the important neuro transmitters used by the NS, so as a part of the PNS the bacteria are an important source of
                        neurotransmitter production. An impaired microbiota means reduced neurotransmitter production. The bidirectional pathway
                        between the PNS and CNS is profound with dysbiosis being shown to impair vagus signalling to the point of down regulating
                        protein synthesis in the hippocampus which recovers with rescue of the GIT.

                    </p>

                    <p>

                        As the GIT breakdowns in dysbiosis, bacteria migrates to the GIT lining causing permeability and access to circulation causing
                        inflammatory immune responses and toxic bi-products such as LPS which have been shown to cause nervous system inflammation
                        once entering the systemic circulation. In fact, tiny amounts of injected LPS have been shown to cause low mood and anxiety
                        symptoms. Depression is accompanied by the activation of immune–inflammatory pathways, and increased IgM ⁄ IgA responses to
                        lipopolysaccharide (LPS) of gram-negative commensal bacteria, and autoimmune reactions directed against O&NS-modified
                        neopepitopes. This indicates that depression may be at least in part, created by bacterial translocation.

                    </p>

                    <p>

                        There is emerging evidence that these mechanisms can also imply mitochondrial dysfunction. However, it is also likely that
                        deteriorating mitochondrial functioning contributes to the types of symptoms present in TRD and among other health problems.
                        If this is the case then compounds like Mg orotate and probiotics might be especially important for treatment. So what causes
                        dysbiosis? The causes are varied and can relate to the quality of infantile GIT bacterial seeding, diet, lifestyle, and some
                        extent genetics, but the most important factor with regard to mental health is stress. Chronic stress is well established as
                        being able to cause dysbiosis in animal models, and dysbiosis resolves with removal of the stressor. As anxiety
                        is a common presentation in most mental health disorders there is a natural link with dysbiosis in the development of TRD. In
                        fact, a number of anti-depressants have a significant anti-anxiety effect, and in a similar way, effective psychological treatment
                        reduces stress and anxiety. It is also of interest that certain classes of anti-depressants have significant anti-inflammatory
                        effects in the nervous system. As emerging evidence suggest dysbiosis might be relatively common in depression, correcting
                        dysbiosis might be an important starting point in the treatment of TRD. There are classes of bacteria that have demonstrated
                        significant positive effects on mood and anxiety when administered in a probiotic supplement or drink.

                    </p>

