                    <h3>Probiotic treatment</h3>

                    <p>

                        Our final exploratory study in this series has begun and we are examining the impact of some of these specific strains that
                        influence mood along with orotic acid which was supplied as the Medlab NRGBioticTM formula, co-administered with SSRI medication.
                        Therapeutic doses are achieved at four capsules a day. In the study to date, n=10 participants that meet the same diagnostic TRD
                        criteria as our previous studies have completed the two month experimental and two month follow up study and recruitment is ongoing.
                        The early results are promising, with improvement close to remission by two months, as at this stage numbers through this study
                        are small and two outliers have inflated symptom scores. The removal of these two outliers results in the main group achieving
                        remission. It is of interest that the outliers were not responsive to the protocol and had no change which was the case in our
                        previous studies. At follow up after two months without the supplement participants are well into relapse.

                    </p>

