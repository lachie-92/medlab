                    <h3>Conclusion</h3>

                    <p>

                        While more research is needed into the role of these products as adjuvant treatment for TRD, preliminary evidence suggests that
                        attempting to correct underlying physiological imbalances, especially rescuing dsybiosis, might be a preferable starting point
                        for treatment. The combination of orotate and probiotics might be particularly useful given their synergistic actions in the
                        nervous system, their immune system and energy metabolism correcting effects. For those interested in the strains of probiotic
                        bacteria being used in this study please refer to the NRGBioticTM ingredient list. Given the debilitating and chronic nature of
                        TRD, novel approaches discussed in this brief paper are safe in conjunction with standard medications. They have a minimal side
                        effect profile and may offer benefits to a group of people for who there are few effective treatment options other than combining
                        additional classes of psychoactive medications with increasing intolerability for patients due to side effects

                    </p>

