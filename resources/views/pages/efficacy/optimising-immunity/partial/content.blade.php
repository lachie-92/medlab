                <div class="panel-body medlab_panel_content">
                    <h3 style="text-align: center">
                        <strong>Optimising Immunity in preparation for cold and flue season</strong>
                    </h3>
                    <h5 style="text-align: center">
                        <strong>Dr Samantha Coulson, Professor Luis Vitetta and Isabelle Ibrahim</strong><br>
                        <strong>Medlab Clinical Ltd</strong>
                    </h5>
                    <br>
                    <p style="text-align: justify;">
                        During the colder months of the year, infectious and viral illness are widespread and seemingly unavoidable
                        causing a major disruption to the productivity of individuals and households, whilst simultaneously putting
                        a strain on the primary healthcare system and the community in general. Epidemiological reports show that in
                        New South Wales the incidence of respiratory illness during winter has undergone a 3-fold increase (see graph 1)
                        over the past four years, with a remarkable spike in the number of reported cases in 2015.<sup>1,2</sup>
                    </p>

                    <br>
                    <img class="img-responsive" style="display: block; margin-left: auto; margin-right: auto;" alt="Total weekly counts of ED visits for influenza-like illness" src="/img/efficacy/optimising-immunity/nsw_health_department_graph_2016.png" title="Total weekly counts of ED visits for influenza-like illness">
                    <p style="text-align: center"><strong>Graph 1: total weekly counts of ED visits for influenza-like illness, from January – 8 May (NSW Health Department, 2016)</strong></p>
                    <br>

                    <p style="text-align: justify;">
                        Notifiable respiratory diseases such as pneumonia and influenza are more closely monitored and therefore more
                        significantly reflected in the statistics, however the spread of seasonal cold which is often equally as unpleasant
                        and disruptive, holds a greater impact for industrialised communities due to the sheer number of people infected.<sup>3</sup>
                        Acute respiratory infections are extremely common and are often not successfully treated using antibiotics,
                        meaning that the best defence is a healthy immune system, as well as a strategy to minimise the severity and
                        duration of cold and flu symptoms.<sup>3</sup> Last year, the incidence of influenza, pneumonia and acute respiratory
                        illness more than doubled compared to 2014, and with the high flu-risk period fast approaching, investing in an
                        immune support supplement is a wise choice.<sup>1</sup> With all too common poor dietary and lifestyle choices partnered
                        with unavoidable stressors, the provision of a scientifically formulated supplement to support your patient’s innate
                        immune responses may help in reducing the incidence, duration and symptom severity of upper respiratory tract infections.
                    </p>

                    <p style="text-align: justify;">
                        <br>
                        <strong>Ascorbic Acid:</strong><br><br>
                        Ascorbic acid is a water-soluble vitamin essential for healthy immune system activity and can only be obtained from
                        the diet due to lack of endogenous synthesis in humans. It is an important catalyst for extensive biochemical reactions
                        and enzymatic processes, and further is a vital redox cofactor.<sup>6</sup> Pharmacokinetic data demonstrate that plasma
                        ascorbic acid levels are tightly regulated, which significantly impacts on the bioavailability of orally administered
                        vitamin C supplements.<sup>4</sup> Optimum bioavailability of ascorbic acid occurs between 200 – 500 mg per day and as
                        the oral dosage increases, bioavailability decreases and excess ascorbic acid is excreted in urine. Plasma ascorbic acid
                        levels plateau at a dose between 500 – 1000 mg and does not increase with increasing doses (see Figure 1).<sup>4</sup>
                        Data demonstrates that circulating WBCs become saturated at an intake of 100 mg per day. Ascorbic acid bioavailability
                        in plasma is near 100% at a single dose of 200 mg but at higher oral vitamin C doses, bioavailability declines significantly
                        with a 1250 mg dose resulting in less than 50% bioavailability (see Figure 2).<sup>4</sup> As the oral dose of ascorbic acid
                        increases so does its excretion in urine (see Figure 3). Elevated doses may have adverse consequences as a 1000 mg dose has
                        been shown to elevate urine uric acid and oxalate levels. High doses of ascorbic acid are not recommended, as there is no
                        clear benefit of excess excreted or unabsorbed vitamin C.<sup>4</sup> Physiologically, ascorbic acid is required for the
                        effective anti-microbial function of WBCs acting as a reducing agent. It is also a key compensatory factor in the regulation
                        of plasma glutathione concentration.<sup>10</sup> The prophylactic use of ascorbic acid may reduce the incidence, duration
                        and severity of the common cold.<sup>11,15</sup>
                    </p>

                    <br>
                    <img class="img-responsive" style="display: block; margin-left: auto; margin-right: auto;" alt="ascorbic acid figures 1-3" src="/img/efficacy/optimising-immunity/ascorbic_acid_figures.png" title="ascorbic acid figures 1-3">
                    <br>

                    <p style="text-align: justify;">
                        <br>
                        <strong>Glutathione:</strong><br><br>
                        Glutathione is an intrinsic tripeptide essential for optimal immune function; this is exemplified predominantly in its
                        capacity to facilitate cellular protection and repair as a vital cofactor in the redox process.<sup>5,9</sup>
                        Glutathione is critical for the physiological role of ascorbic acid in white blood cell anti-microbial activity.
                        When exposed to bacteria, neutrophils oxidize extracellular ascorbic acid to form dehydroascorbic acid that is transported
                        into the neutrophil and rapidly reduces back to ascorbic acid within the cell by the redox enzyme glutaredoxin that
                        requires glutathione as a cofactor.<sup>16</sup> As a result of the recycling of ascorbic acid, the internal concentration
                        of ascorbic acid within the WBC increases 10-fold in which it functions to quench oxidants generated during phagocytosis,
                        a natural and controlled physiological process (see Figure 4).<sup>16</sup>
                    </p>

                    <br>
                    <img class="img-responsive" style="display: block; margin-left: auto; margin-right: auto;" alt="DHA and AA transport and recycling in neutrophils" src="/img/efficacy/optimising-immunity/DHA_and_AA_transport_and_recycling_in_neutrophils.png" title="DHA and AA transport and recycling in neutrophils">
                    <p style="text-align: justify"><strong>Figure 4. Dehydroascorbic acid (DHA) and ascorbic acid (AA) transport and recycling in neutrophils.<sup>10</sup></strong>
                        AA and DHA are transported differently into the cell. With activation (i.e. bacterial exposure), neutrophils
                        secrete reactive oxygen species that oxidize extracellular AA to DHA that is then rapidly transported into
                        the neutrophil via the glucose transporters, GLUT1 and GLUT3. Intracellular DHA is immediately reduced to AA
                        via Glutaredoxin (GRX), a glutathione requiring redox enzyme. As a result of DHA transport and reduction, a
                        10-fold higher AA internal concentration is achieved compared to activity of the AA transporter alone. AA may
                        quench oxidants generated during phagocytosis, a regulated physiological occurrence.
                    </p>
                    <p>
                        GSH: reduced glutathione; GSSG: oxidized glutathione; GRD: glutathione reductase
                    </p>

                    <p style="text-align: justify;">
                        <br>
                        <strong>Zinc:</strong><br><br>
                        Zinc is necessary for numerous metabolic functions, particularly gene expression; cell growth and differentiation.<sup>11</sup>
                        It is a critical nutrient for immune function due to its role in supporting a range of immune reactions.<sup>17</sup>
                        Zinc is a vital component of many proteins, over 300 enzymes; and is regarded as a key nutrient in the support of
                        cellular mediators of innate immunity.<sup>17</sup> Zinc functions in the maturation of T cells and is also responsible
                        for the activity of natural killer cells as well as waste management in WBC.<sup>18</sup> Zinc together with ascorbic acid
                        is shown to produce additive effects in supporting immune function; and together have shown to be effective in reducing
                        the severity and/or duration of the common cold.<sup>11</sup>
                    </p>

                    <p style="text-align: justify;">
                        <br>
                        <strong>Manuka Honey:</strong><br><br>
                        Manuka honey has been used as a traditional anti-microbial therapy for the treatment of infection.<sup>14</sup> Honey
                        has well documented anti-bacterial activity, together with anti-viral and anti-fungal properties.<sup>12,19</sup>
                        The anti-inflammatory effects of Manuka honey are effective in assisting the recovery from the common cold; as well
                        as treating symptoms of upper-respiratory tract infections.<sup>20,21</sup> Emerging evidence has illustrated that honey added
                        to oral rehydration solution (ORS) improved recovery in children following episodes of diarrheal illness.<sup>13</sup>
                    </p>

                    <p style="text-align: justify;">
                        <br>
                        <strong>Probiotics:</strong><br><br>
                        The gastrointestinal commensal microbiota significantly influences the health of humans, being supported by
                        extensive scientific evidence. Whilst there are many publications exemplifying the range of positive benefits of probiotic
                        bacteria, the mechanisms by which they benefit health and the specific species and strains are of a growing interest within
                        the scientific community. A Cochrane Review published in 2011 examined clinical studies assessing live microorganism
                        intervention in cold and flu treatment, and found that probiotic bacteria including various <em>Lactobacillus</em> and
                        <em>Bifidobacterium</em> species were effective in reducing the symptoms of acute upper respiratory tract
                        infections.<sup>22</sup> Two clinical trials demonstrated that daily consumption of a probiotic product containing
                        <em>Lactobacillus paracasei</em> increased the antibody response (IgG) to influenza vaccination in elderly
                        participants.<sup>23,24</sup> Additional research has shown that consumption of probiotics containing
                        both <em>Lactobacillus paracasei</em> and <em>Bifidobacterium lactis</em> increases the antigen-specific immune
                        response (IgG and sIgA) of patients receiving the influenza vaccine, further supporting their immune
                        modulatory effects.<sup>24</sup> In a 2014 systematic review it was determined that the use of probiotics
                        in children is effective in diminishing the incidence and the severity of the symptoms of upper respiratory
                        tract infections.<sup>32</sup>
                    </p>

                    <p style="text-align: justify;">
                        <br>
                        <strong>Arabinogalactan:</strong><br><br>
                        Arabinogalactans are highly branched, water-soluble polysaccharides with a high molecular weight that are
                        found in a wide variety of plants. Arabinogalactans specifically from Larch are an excellent dietary fiber
                        supplement, which is capable of increasing friendly bacteria volume as well as short-chain fatty acid
                        production.<sup>25,26</sup> As well as being shown to be effective against the common cold infection,
                        Larch arabinogalactan has also been shown in animal studies to stimulate natural killer cell
                        cytotoxicity <em>in vitro</em>, demonstrating increases in white blood cell counts i.e.
                        neutrophils and eosinophil’s.<sup>27,28</sup> ResistAid&trade; is a proprietary arabinogalactan extracted
                        from Larch (<em>Larix laricina</em>) supplied by Lonza Ltd, Switzerland. A number of human clinical studies
                        have demonstrated ResistAid's&trade; immunomodulatory activity, including a study which demonstrated a daily
                        dose of 4.5 g significantly increased IgG antibody response of healthy volunteers to the pneumococcal
                        vaccine when compared to placebo.<sup>28,29</sup> In a more recent study it was demonstrated that ResistAid&trade;
                        at a dose of 1.5 g daily significantly increased the IgG antibody response to tetanus vaccine compared to
                        placebo.<sup>29</sup> ResistAid&trade; has clinical evidence of being well tolerated in healthy individuals
                        with no demonstrable side-effects.<sup>30,31</sup>
                    </p>
                    <p style="text-align: justify;">
                        The scientific evidence supporting these immune-modulatory nutrients and bacteria provides a plausible
                        therapeutic application for prophylactic and symptomatic treatment of upper respiratoy tract infections
                        in both children and adults.
                    </p>
