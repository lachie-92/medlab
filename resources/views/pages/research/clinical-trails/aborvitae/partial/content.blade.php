                    <h4 class="text-center">Medlab Clinical Research Centre is now enrolling adults with prediabetes for a clinical research study.</br>
                    A natural supplement may reduce blood sugar levels in people with prediabetes.</h4>
                    <hr>


                    <h4>Background</h4>
                    <p>Pre-diabetes affects about 16.4% of Australian adults. People with prediabetes are at increased risk of developing type 2 diabetes, and heart disease.</p>
                    <p>The research study is testing whether a natural supplement containing French Maritime Pine Bark Extract, Papain and Aloe Vera (Arborvitae Health and Wellbeing Supplement â€“ AHWS) will help to improve blood sugar levels in people with prediabetes.</p>
                    <p>This study seeks men and women who have prediabetes or suspect they have prediabetes. Participants are required to attend 5 study visits at the research site (Alexandria, NSW) over 12 weeks  as well as undergo 4 blood tests and will be reimbursed $250 for travel expenses at study completion. In addition, the study sponsor Arbovitae Health and Wellbeing will send to all participants who have completed the study 4 x 1L bottles of the supplement, valued at $239.50. The study is a placebo controlled double blind randomized trial, which means that if found eligible to participate, you could be administered either the supplement or a placebo.</p>
                    <h4>Benefits to Participation</h4>
                    <li>You may experience an improvement in blood sugar.</li>
                    <li>You will be reimbursed $250 for travel expenses.</li>
                    <li>All medication, tests and medical care required as part of the research project will be provided to you free of charge.</li>
                    <li>You will receive 4 x 1L bottles of the Arbovitae Health and Wellbeing supplement, valued at $239.5</li>
                    <li>You will be helping to advance medical research.</li>

                    <h4>Your Rights</h4>
                    <li>If you decide to participate in the study and later feel that you no longer wish to be part of it, you may withdraw at any time.</li>
                    <li>Your records relating to this study and any other information received will be kept strictly confidential, except as required by the law.</li>
                    <li>A qualified Registered Nurse, directed and supervised by the study Principal Investigator (Professor of Medicine) will monitor your health as it relates to the study.</li>

                    <h4>Who Can Participate?</h4>
                    <li>Men and women aged 18+ who:</li>
                    <li>Have prediabetes or suspect they have prediabetes</li>
                    <li>Have a BMI>25</li>
                    <li>Are able to attend 5 study visits at the research site over approximately 12 weeks</li>
                    <li>Do not have a diagnosis of Type II Diabetes Mellitus</li>
                    <li>Do not take Anti-Obesity medications such as Orlistat</li>
                    <li>Do not take oral blood glucose-lowering medications such as Sulfonylureas, Biguanides, Alpha-glucosidase inhibitors, Thiazolidinediones</li>
                    <li>Do not take any dietary supplements such as herbal, multivitamin or mineral, probiotics, fish oils etc, prior to commencing study. However, if prescribed by their doctor they must NOT stop taking any form of dietary supplement. This will however make them ineligible to participate in the study</li>
                    <li>Are pregnant or nursing of an infant;</li>
                    <li>Are commencing lifestyle interventions such as dietary changes and increasing exercise duration/intensity</li></br>
