                    <h3>
                        <strong>What is nanotechnology?</strong>
                    </h3>
                    <p style="text-align: justify;">
                        Nanotechnology converts fat-soluble and poorly water-soluble compounds into highly water-soluble
                        particles. The process creates a clear aqueous solution of these compounds in high
                        concentrations. This results in very stable therapeutic preparations that greatly enhance the
                        compound's absorption and also the rate at which the compound is absorbed. This in particular
                        allows absorption across the oral mucosal lining as opposed to Intestinal absorption.
                    </p>
                    <h3>
                        <strong>Our research</strong>
                    </h3>
                    <p style="text-align: justify;">
                        Medlab has achieved a successful world first lodgment for a patent application pertaining to a
                        unique delivery system that converts such compounds into nanosized particles, certified to
                        measure less than 0.2 microns. Nano-formulations, by virtue of their size, are known for
                        circumventing the obstacles of poor pharmacokinetics. The resulting aqueous solubility of the
                        compound ensures its transport across the cellular membrane. This patented technology will be
                        applied to both nutritional and pharmaceutical compounds in the near future.
                    </p>
                    <p style="text-align: justify;">
                        Our research demonstrates that nanotechnology increases plasma and serum levels of fat-soluble
