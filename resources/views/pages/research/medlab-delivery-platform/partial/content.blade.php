                    <h3>
                        <strong>How NanoCelle works - differences to Emulsions</strong>
                    </h3>
                    <p style="text-align: justify;">
                        Emulsions typically consist of at least 2 immiscible liquids (usually oil and water) with one
                        of the liquids being dispersed as small spherical droplets into the other. Emulsions are
                        thermodynamically unfavorable systems that tend to break down over time due to a variety of
                        physicochemical mechanisms including gravitational separation, flocculation, coalescence and
                        change of structure.<sup>3</sup> Conventional oil-in-water emulsions do not produce
                        water-soluble particles (Fig 3); they remain water-insoluble and thus still require efficient
                        processing by the body to create particles that are able to be absorbed across the mucosal e
                        pithelium of the small and large intestine. Our nanotechnology creates water-soluble aqueous
                        solutions (Fig 4).
                    </p>
