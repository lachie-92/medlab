                    MEDLAB'S CELL LINES
                </div>
                <div class="panel-body medlab_panel_content">

                    <p style="text-align: justify;">
                        In order for Medlab to undertake research in the realm of novel therapeutics relevant to Chronic Diseases,
                        Medlab has established intestinal cell lines (i.e., caco–2 / goblet cells combined) in a monolayer culture
                        in an effort to undertake and investigate pro–inflammatory induced permeability studies and rescue with
                        Medlab's premium probiotic species / strains.
                    </p>
                    <p style="text-align: justify;">
                        There is recognised an ever increasing therapeutic role in everyday administration of probiotic formulations.
                        As such Medlab has utilised its international collaborative network to source key probiotic bacteria so as
                        to then establish a local storage site of mother strains. Mother probiotic strains have been sourced from
                        primary facilities in the northern hemisphere and shipped to Medlab in agar plates under strict controlled
                        temperature and humidity conditions.
                    </p>
                    <p style="text-align: justify;">
                        It is from these starting cell lines Medlab grow, inoculate and develop highly potent viable probiotic bacteria.
                    </p>
                    <p>
                        <img class="img-responsive" style="vertical-align: middle; display: block; margin-left: auto; margin-right: auto; width:500px;" title="Agar plate" src="/img/research/agar_plate.png" alt="Agar plate">
                    </p>
                    <p style="text-align: justify;">
                        Probiotic growth media are prepared that are specific for each probiotic genera (e.g., <em>Lactobacilli</em> or <em>Bifdobacteria</em>)
                        prior to an inoculation phase; this in order to maximise specific strain culture growth for later in vitro permeability /
                        therapeutic response studies.
                    </p>
                    <p style="text-align: justify;">
                        All inoculation and growth phases of probiotic bacteria are conducted in a temperature and humidity
                        controlled anaerobic chamber that ensures probiotic strain viability.
                    </p>
                    <p>
                        <img class="img-responsive" style="vertical-align: middle; display: block; margin-left: auto; margin-right: auto; width:500px;" title="Laboratory work" src="/img/research/laboratory_work.png" alt="Laboratory work">
                    </p>
                    <p style="text-align: justify;">
                        Medlab does not manufacture probiotic end products in–house. On completion of laboratory studies conducted at Medlab's Sydney facility,
                        a probiotic strain is ready for concentration escalation, and  instructions are then sent to a Medlab approved international manufacturer
                        with demonstrated capabilities in handling probiotic products.
                    </p>
                    <p style="text-align: justify;">
                        The end–result is Medlab's strain designations, indicating probiotic bacterium, inoculation and
                        concentration escalation process,  patents and research.
                    </p>
                    <p>
                        <img class="img-responsive" style="vertical-align: middle; display: block; margin-left: auto; margin-right: auto;" title="Species table" src="/img/research/species_table.jpg" alt="Species table">
                    </p>
                    <p style="text-align: justify;">
                        To date, Medlab has published numerous peer reviewed articles summarising the clinical evidence relevant to the efficacy of probiotics in different clinical scenarios...
                    </p>
                    <p style="text-align: justify;">
                        <a href="/education/publications">https://www.medlab.co/education/publications</a>
                    </p>
                    <p style="text-align: justify;">
                        Many of these published studies highlight probiotic bacterial strains that are present in Medlab's probiotic formulation as for example in MultiBiotic, NRGBiotic and GastroDaily.
                    </p>
                    <p style="text-align: justify;">
                        Furthermore Medlab holds a multitude of PCT, pending and innovative patents justifying uniqueness and novelty of the probiotic bacteria utilised in numerous current formulations.
                    </p>
