                                addressing the problem where it often starts: the gut.
                            </p>
                        </li>
                        <li>
                            <p>
                                We create innovative new medicines, such as medicines derived from bacterial
                                metabolites and bioavailable cannabinoids for the management of pain associated
                                with cancer, HIV/AIDS, and chronic disease
                            </p>
                        </li>
                        <li>
                            <p>
                                We use nanotechnology to deliver medicines in nanoparticles that can be more readily
                                absorbed when administered directly into the mouth or nose.
                            </p>
                        </li>
                        <li>
                            <p>
                                We create next "gen" solutions for pharmaceuticals for better delivery and improved
                                pharmacokinetics, such as statins, utilising technologies that will markedly improve
                                health outcomes and diminish side effects.
                            </p>
                        </li>
                        <li>
                            <p>
                                We <a class="medlab_panel_content_link" href="/research">
                                focus on delivering new medicines</a> for:
                            </p>
                            <ul>
                                <li>
                                    <p>
                                        Obesity management, specifically in the pre-diabetic and type II diabetic areas,
                                        and non alcoholic fatty liver disease (NAFLD)
                                    </p>
                                </li>
                                <li>
                                    <p>
                                        Chronic Kidney Disease (CKD), specifically in the early to moderate stages
                                    </p>
                                </li>
                                <li>
