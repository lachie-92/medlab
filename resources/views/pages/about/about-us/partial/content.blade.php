                    ABOUT MEDLAB
                </div>
                <div class="panel-body medlab_panel_content">
                    <div style="height: 5px; text-align: justify;">&nbsp;</div>
                    <p>
                        Medlab Clinical is a medical research and development facility researching and formulating
                        novel bio-therapeutics (e.g., nutraceuticals and pharmaceuticals) to improve health outcomes
                        in early to moderate stage chronic diseases such as chronic kidney disease and
                        pre-diabetes/obesity.
                    </p>
                    <p>
                        There are <a class="medlab_panel_content_link" href="/research">5 core research programs</a>
                        that are at various stages of investigation, Medlab Clinical researches believe that the
                        company is at the forefront of new bio-therapeutic discoveries, and, as such posits to
                        progress its laboratory and clinical research that can bridge unmet needs in the ever
                        increasing global trends in chronic diseases.
                    </p>
                    <p>
                        Why chronic diseases...?
                    </p>
                    <p>
                        "The global epidemic of chronic diseases that are associated with obesity and its disease
                        correlated sequelae leads to an ever increasing cost to the community. Therefore biomedical
                        research that is focused on improving the health of the community is of paramount importance.
                        Global population trends in chronic disease prevalence are reporting increasing numbers of
                        patients diagnosed with various chronic diseases and an acceleration in disease severity.
                        Medlab Clinical is dedicated to develop novel therapeutic intervention that would improve
                        chronic disease health outcomes, whether these be in prevention, maintenance support, or
                        curative." SEAN HALL CEO/MD
                    </p>

                    <br>

                    <p>
                        <strong>Understanding chronic diseases</strong>
                    </p>
                    <p>
                        Understanding the impact of chronic diseases is vital; the
                        <a class="medlab_panel_content_link" href="http://www.aihw.gov.au/chronic-diseases/">
                        Australian Institute of Health Welfare (AIHW) claims chronic diseases</a>
                        (in Australia) accounted for 90% of all deaths in 2011. (extracted
                        <a class="medlab_panel_content_link" href="http://www.aihw.gov.au/chronic-diseases/">
                        http://www.aihw.gov.au/chronic-diseases/</a>)
                    </p>

