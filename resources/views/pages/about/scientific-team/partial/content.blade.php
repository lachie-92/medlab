                    <h3>
                        <strong>Professor Luis Vitetta</strong>
                    </h3>
                    <p>
                        <strong>Director of Medical Research</strong>
                    </p>
                    <p>
                        <img style="float: left; margin-right: 10px;" title="Luis Vitetta Portrait" src="/img/about/luis.jpg" alt="Luis Vitetta Portrait" width="125">
                    </p>
                    <p style="text-align: justify;">
                        Luis Vitetta is Director of Medical Research at Medlab and Adjunct Professor at the University
                        of Sydney, Sydney Medical School.
                    </p>
                    <p style="text-align: justify;">
                        Luis's expertise includes probiotics, the human biome, nutraceuticals, nutrition, clinical
                        trials, epidemiology', integrative and complementary medicine.
                    </p>
                    <p style="text-align: justify;">
                        Between 2007-2013 Luis was the Director and Associate Professor of the Centre for Integrative
                        Clinical and Molecular Medicine at The University of Queensland, School of Medicine. Luis is
                        an Honours Graduate from Monash University (1981) in the Department of Biochemistry - Faculty
                        of Medicine, and a PhD Graduate from the University of Melbourne Faculty of Medicine's
                        Department of Surgery (1985) where he studied the epidemiological and aetiological factors of
                        biliary disease. He has also studied medicine at the National University of La Plata Medical
                        School in his native Argentina.
                    </p>
                    <p style="text-align: justify;">
                        Luis was the chief investigator and the recipient of a National Institute of Complementary
                        Medicine grant for the NICM Collaborative Centre for Transitional/Preclinical and Clinical
                        Research in Nutraceuticals and Herbal Medicines.
                    </p>
                    <p style="text-align: justify;">
                        Luis was a Senior Research Fellow at the Centre for Molecular Biology and Medicine,
                        Epworth Medical Centre in Melbourne.
                    </p>
                    <p style="text-align: justify;">
                        Between the years of 2000-2005 Luis was the Deputy Head and Director of Research of the
                        Graduate School of Integrative Medicine at Swinburne University, where he assisted in
                        implementing post graduate courses for medical graduates in the areas of nutrition and
                        environmental medicine and integrative medicine. He has served as a member of the Cancer
                        Council's epidemiology group, has been an alternate director of the Victorian Public Health
                        Research and Education Council and a member of its research committee. He was a member of the
                        executive board of the Australian Research Centre for Complementary and Alternative Medicine.
                        He was also an honorary affiliate with the Department of Medicine, Nursing and Health Sciences,
                        Monash University.
                    </p>
                    <p style="text-align: justify;">
                        Earlier appointments include Senior Research Associate with the University of Melbourne's
                        Faculty of Medicine - Centre for Palliative Care at Caritas Christi Hospice and St Vincent's
                        Hospital and a University of Melbourne Research Fellow with the Faculty of Medicine and the
                        Department of Surgery at the Austin and Repatriation Medical Centre.
                    </p>
                    <p style="text-align: justify;">
                        Professor Vitetta has research interests in nutrition, functional foods and immune function,
                        as well as Mind Body Medicine. He was the first internationally funded Australian researcher
                        investigating Chinese Herbal Medicines in a trial with HIV patients. He is frequently invited
                        to speak at local and international conferences.
                    </p>
                    <h3>
                        <strong>Dr Rachel Manuel</strong>
                    </h3>
                    <p>
                        <strong>Senior Research Scientist</strong>
                    </p>
                    <p>
                        <img style="float: left; margin-right: 10px;" title="Rachel Manuel Portrait" src="/img/about/rachel1.jpg" alt="Rachel Manuel Portrait" width="125">
                    </p>
                    <p style="text-align: justify;">
                        Rachel Manuel completed a PhD in 2013 from the University of Claude Bernard, Lyon (France).
                        She examined the implication of a collagen-derived fragment in tumour growth and angiogenesis
                        regulation. Her present research work at Medlab focuses on the in vitro effects of probiotic
                        strains. The purpose of this research is to examine their ability to restore the integrity of
                        the gut epithelial barrier, which is impaired in patients with chronic kidney disease.
                    </p>
