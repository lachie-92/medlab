                    </h3>
                    <p>
                        <strong>Senior Research Scientist (Microbiology)</strong>
                    </p>
                    <p>
                        <img style="float: left; margin-right: 10px;" title="Joyce Zhou Portrait" src="/img/about/joyce.jpg" alt="Joyce Zhou Portrait" width="125">
                    </p>
                    <p style="text-align: justify;">
                        Joyce Yusi Zhou has a PhD in Chemical and Biomedical Engineering from the Nanyang Technological
                        University in Singapore. Her research area focused on metabolome and proteome analysis of
                        bacterial response to antimicrobial peptides. Joyce also holds a Master of Science in Food
                        and Nutrition and Bachelor of Science in Biotechnology. Her current research at Medlab focuses
                        on the metabolomics of probiotics and other bacteria.
