                    <h3>
                        <strong>Medlab Clinical</strong>
                    </h3>
                    <p style="text-align: justify;">
                        Medlab Clinical Ltd (Medlab) was formed by Sean Hall after he sold his family's companies FIT-BioCeuticals Limited
                        and Hall Drug Technologies to Blackmores Limited
                        on the first of July 2012.
                    </p>
                    <p style="text-align: justify;">
                        Medlab is a public listed company (ASX:MDC) based in Sydney, Australia.
                    </p>
                    <br>
                    <h3>
                        <strong>lnMed Technologies</strong>
                    </h3>
                    <p style="text-align: justify;">
                        lnMed Technologies based in California USA, is a subsidiary of Medlab, specialising in unique
                        delivery platforms for application in medicines, cosmetics and animal health. lnMed's ground
                        breaking development of micellisation in the 1980s paved the way for Medlab's developing
                        <a class="medlab_panel_content_link" href="/research/medlab-delivery-platform">NanoCelle</a>
                        technology to enhance the bioavailability of otherwise poorly absorbed medical compounds.
                    </p>
                    <p style="text-align: justify;">
                        Medlab and lnMed's integrated approach accelerates their joint research and development program.
                    </p>
