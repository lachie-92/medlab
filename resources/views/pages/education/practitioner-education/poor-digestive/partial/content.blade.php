                    <h3 style="text-align: center; margin-top: 10px; margin-bottom: 20px;">
                        <strong>POOR DIGESTIVE FUNCTION</strong>
                    </h3>

                    <p>
                    Several factors are known to contribute to the development of poor digestive function including stress, medications, poor eating habits and dietary choices,
                    dysbiosis, ageing, illness, injury/trauma and toxic exposure.<sup>1,2</sup> Symptoms of digestive dysfunction can have an debilitating impact on an individuals
                    quality of life. Symptoms range in severity and can include indigestion, reflux, heartburn, constipation, diarrhoea, nausea, abdominal pain and bloating,
                    steatorrhoea, flatulence, unintentional weight loss and undigested food particles in the stool.<sup>2</sup> In conjunction with addressing dietary and lifestyle choices;
                    therapeutic options available to reduce symptomology and improve digestive function and nutrient absorption include probiotic bacterial species and digestive enzymes.
                    </p>

                    <p>
                    Commensal gastrointestinal tract (GIT) microbiota play a critical role in digestive function contributing to extensive metabolism of dietary compounds (and drugs),
                    regulating energy metabolism and maintaining healthy GIT mucosal function and immune modulation.<sup>3</sup> Alteration of the GIT bacteria can occur through stress, dietary
                    changes, medication use, inflammatory conditions and illness. There is extensive evidence to support the supplementation of probiotics for improving digestive
                    function, reducing symptoms and improving GIT barrier function.<sup>4,5,6</sup>
                    </p>

                    <p>
                    Digestive enzymes available for supplementation include pharmaceutical grade enzymes such as Creon and Creon Forte, which are porcine sourced, therefore not
                    suitable for vegetarians. Also available are fungal (from Aspergillus oryzae and Rhizopus oryzae) and plant-derived (bromelain and papain) enzymes. Plant derived
                    proteolytic enzymes include bromelain (sourced from Ananas comosus) and papain (sourced from Carica papaya). Aspergillus oryzae and Rhizopus oryzae are filamentous
                    fungi that secrete vast amounts of digestive enzymes such as amylase, protease and lipase.<sup>7,8</sup> Commensal bacteria are also capable of producing enzymes that assist
                    with digestion.<sup>9</sup> Proteolytic enzymes breakdown proteins into polypeptides, dipeptides and amino acids; amylolytic enzymes (amylase) hydrolyze starches and other
                    carbohydrates (not including cellulose) into disaccharides and trisaccharides and lipolytic enzymes (lipase) hydrolyze fats into fatty acids and monoglycerides.<sup>1</sup>
                    </p>

