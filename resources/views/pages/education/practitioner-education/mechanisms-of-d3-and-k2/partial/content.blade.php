                    <p>
                        <strong>Who will benefit:</strong>
                    </p>

                    <p>
                        <ul>
                            <li>Individuals with medically diagnosed vitamin D deficiency.<sup>2,10</sup></li>
                            <li>Individuals with vitamin K deficiency.<sup>4</sup></li>
                            <li>Individuals at risk of bone-disorders and bone weakness.<sup>6</sup></li>
                            <li>Post-menopausal women with arterial stiffness.<sup>7</sup></li>
                            <li>Post-menopausal women with bone-disorder or osteoporosis.<sup>8</sup></li>
                            <li>Individuals recovering from muscular-skeletal or nerve injury.<sup>12</sup></li>
                            <li>Those requiring increased calcium uptake and metabolism.<sup>19</sup></li>
