                    <h3 style="text-align: center; margin-top: 10px; margin-bottom: 20px;">
                        <strong>MECHANISMS OF ACTION FOR VITAMIN D3 AND K2</strong>
                    </h3>

                    <p>
                        <strong>Vitamin D3:</strong>
                    </p>
                    <p>
                        Vitamin D3 (cholecalciferol) is a lipid-soluble vitamin that is naturally synthesised in the body
                        from ultraviolet light exposure, however it’s availability may be reduced in people with a dark complexion,
                        the elderly and in those with chronic kidney disease.<sup>10</sup> Vitamin D deficiency is becoming more
                        common in the urban landscape due to occupational and lifestyle factors associated with current standards
                        of modern living; as well as the widespread use of sunscreens, and seasonal and environmental
                        factors.<sup>11</sup> Vitamin D3 aids in dietary calcium and phosphorus absorption, which is important
                        for maintaining healthy BMD and is used in the treatment and prevention of bone disorders such as rickets,
                        osteomalacia and is essential in preventing the development of osteoporosis in later life.<sup>2,3,10</sup>
                        Supplementation with D3 in conjunction with calcium has been effectively administered in primary care to
                        prevent fractures; and there is also evidence to suggest that cholecalciferol has beneficial therapeutic
                        effects on myelination and recovery following nerve injury.<sup>12,13</sup> Vitamin D3 binds to the vitamin
                        D receptor (VDR), which is located on all somatic tissue cells, including immune system cells; D3 is also
                        responsible for the activation or repression of gene expression, and simultaneously acts as a control
                        system for innate and adaptive immune system function including Th<sub>1</sub>, Th<sub>2</sub>, Treg
                        and Th<sub>17</sub> responses.<sup>14,15</sup> Additionally, vitamin D3 is involved in the attenuation
                        of inflammatory responses and chronic conditions, as well as the promotion of healthy respiratory
                        function.<sup>16,17</sup> Because the absorption of dietary calcium within the human body is so dependent
                        on the availability of vitamin D, the need for supplementation in individuals living in a modern-day
                        environment is often necessary to impede the proliferation of preventable disease.<sup>18,19</sup>
                    </p>

