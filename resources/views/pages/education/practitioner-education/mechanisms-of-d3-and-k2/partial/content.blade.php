                    <p>
                        <strong>Vitamin K2:</strong>
                    </p>
                    <p>
                        Vitamin K serves as an obligatory cofactor for an essential carboxylase enzyme that activates seven
                        protein components of the blood-coagulation cascade, and is essential for the post translational
                        carboxylation of glutamate into gamma-carboxyglutamate (Gla) residues in Gla-proteins that are
                        required for clot formation and bone mineralization.<sup>4,5</sup> Carboxylation of the proteins
                        osteocalcin and matrix-Gla which are essential for normal bone metabolism depend on the availability
                        of vitamin K.<sup>4</sup> Whilst vitamin K1 is commonly found in leafy green vegetables, the alternative
                        form vitamin K2 (Menaquinone-7) is long-chain menquinone found in high concentrations in fermented foods
                        such as natto, and holds longer lasting positive effects than other common forms of the vitamin such
                        as K1 and meniquinone-4.<sup>5</sup> A clinical trial determined that long term use of menaquinone-7
                        may improve arterial stiffness in healthy postmenopausal females.<sup>6</sup> Vitamin K2 (menaquinone-7)
                        has been proven to be extremely beneficial for postmenopausal women as supplementation may improve
                        serum vitamin K2 status helping to reduce bone loss.<sup>7</sup>
                    </p>

