                    <h3 style="text-align: center; margin-top: 10px; margin-bottom: 20px;">
                        <strong>WHY AN ACTIVATED FORM OF B12?</strong>
                    </h3>

                    <p>
                    All tissues have a requisite for uridine. Uridine is an important nucleoside precursor in the pyrimidine salvage pathway.<sup>8,9,10</sup> 
                    Except for erythrocytes and the liver and kidney, which maintain de novo pyrimidine biosynthesis and supply other tissues with
                    uridine for salvage, most normal tissues in adults rely on the salvage of uridine from plasma.<sup>9</sup>
                    </p>

                    <p>
                    Uridine also participates in the regulation of several physiological and pathological processes.<sup>11</sup> In the absence of sugar,
                    uridine can serve as an essential precursor for both carbohydrate metabolism and nucleic acid synthesis. Besides the direct
                    salvage to pyrimidine nucleotides, the ribose moiety produced by the phosphorolysis of uridine can either enter the pentose
                    cycle to meet cellular energy requirements or form phosphoribosyl pyrophosphate, which is used in the biosynthesis of purine
                    nucleotides.<sup>11</sup> In the nervous system, uridine acts as a physiological regulator of sleep function and demonstrates the ability
                    to maintain brain metabolism during ischemia and severe hypoglycemia.
                    </p>

                    <p>
                    Coenzyme Q10 (CoQ10) is a compound that is naturally found intracellularly and bound to organelle membranes such as for example
                    the mitochondria, endoplasmic reticulum, and golgi apparatus. CoQ10 is found with the highest concentrations in tissues such as
                    the heart, liver, kidneys, and pancreas. This compound is intricate to the production of adenosine triphosphate (ATP). ATP is
                    essential to all bodily functions, including the generation of energy and a multitude of metabolic functions.<sup>12,13,14</sup>
                    </p>

                    <p>
                    Over the past several decades, research has seen a refocus towards thinking and efforts directed in elucidating the critical
                    inter–relationships that exist between the GIT microbiome and its host. This research has redefined the interactions between
                    gut microbes and vertebrates, now recognising that the microbial active cohort and its mammalian host have shared co–evolutionary
                    metabolic interactions that span millennia. Microbial interactions in the GIT provide the necessary cues for the development of
                    regulated pro– and anti–inflammatory signals that promotes immunological tolerance, metabolic regulation and other factors which
                    may then control local and extra–intestinal inflammation.<sup>15</sup> Furthermore, it is also becoming apparent that the GIT with its commensal
                    cohort is a central regulator for the activities of end organs such as the kidneys, brain, adipose tissue, muscle and liver and as
                    such may provide local prompts that are transmitted extra–intestinally to end organ sites. A scientific insight therefore has
                    emerged that plausibly links the GIT with the physiology of end organ function that may influence health maintenance or trigger
                    and support a disease state. Maintaining a healthy GIT milieu and epithelium with the administration of probiotics may constitute
                    a novel therapeutic strategy for health.
                    </p>

                    <p>
                    Probiotics are those live bacterial cultures that are added to foods (e.g., yoghurts) and employed as dietary supplements, that
                    when orally administered can improve the health of the host beyond their fundamental basic nutritional content.<sup>16</sup> Probiotic bacteria
                    encompass those from different genera (as for example Lactobacilli, Bifidobacterium, Escherichia, Saccharomyces (a yeast),
                    Streptococcus) giving rise to a variety of different species of each genera (i.e., Lactobacillus acidophilus; Lactobacillus
                    bulgaricus, Lactobacillus rhamnosus); that lead to different strains within a species (i.e., Lactobacillus acidophilus La–1,
                    Lactobacillus acidophilus NCFM). This taxonomic differentiation, importantly emphasizing that different strains from the same
                    bacterial species can be different and may elaborate different physiological functions within the GIT<sup>17</sup> whilst exhibiting
                    overlapping or specific therapeutic actions to different organ systems.<sup>18</sup>
                    </p>


