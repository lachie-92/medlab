                    <p>
                        <strong>Breast Milk Microbiome</strong>
                    </p>

                    <p>
                        Breast milk not only provides bioactive molecules to the infant, including: immunocompetent cells, immunoglobulins, fatty acids,
                        oligosaccharides, lactoferrin and antimicrobial peptides, but constitutes one of the main sources of bacteria to the breast fed
                        infant gut and a baby consuming approximately 800 mL/day of milk would ingest between 1 x105 – 1 x 107 bacteria per day. The
                        oligosaccharides play a vital role in driving the diversity of the infant gut microbiota.<sup>1</sup> Potential sources of bacteria present
                        in human colostrum and milk are proposed to be through the Enteromammary pathway, based on a plausible scientific basis. Dendritic
                        cells (DC) are able to penetrate the gut epithelium, extending dendrites into the gut lumen and directly sample/collect the bacteria.
                        Intestinal DCs and macrophages can retain small numbers of live commensal bacteria for several days in the mesenteric lymph nodes.
                        DCs can then translocate these bacteria to other body sites including the lactating mammary gland.<sup>1,4</sup> The proposed method of bacterial
                        transfer from the maternal GIT to the mammary gland is shown in Figure 2.
                    </p>


                    <img class="img-responsive center-block" width="800px;" src="/img/practitioner-education/enteromammary-pathway.png" title="Enteromammary Pathway">
                    <div class="container-fluid">
                        <p>
                            <strong>Figure 2.</strong> Enteromammary Pathway: Dendritic cells in the lamina propria send dendrites into the maternal gut lumen via tight junctions
                            and trap commensal gut bacteria and transport them back to the lamina propria and from here to mesenteric lymph nodes. Once inside dendritic
                            cells and/or macrophages, gut bacteria can spread to other locations such as the mammary gland and therefore into breast milk and possibly the
                            placenta, as there is a circulation of lymphocytes within the mucosal-associated lymphoid system.<sup>4</sup>
                        </p>
