                    <h3 style="text-align: center; margin-top: 10px; margin-bottom: 20px;">
                        <strong>DEVELOPMENT OF THE HUMAN MAMMARY MICROBIOTA</strong>
                    </h3>

                    <p>
                        It is only within the last decade that a complex microbiome was discovered in the placenta, amniotic fluid and breast milk, which were
                        once believed to be sterile. Infants were only thought to be exposed to microbes during birth, i.e. the mother’s vaginal microbiome or
                        that of the hospital staff and surgical environment when delivered via cesarean section. The evolution of the infant gut microbiome does
                        however begin in utero. Continued evolution of the microbiome after birth is influenced by numerous factors including diet, medications,
                        toxins, illness and other stressors.<sup>1,2</sup>

                    </p>

                    <p>
                        In Utero Exposure to Bacteria colonisation of the mucosa of the digestive, respiratory, urogenital tracts and the skin begins before
                        birth. The placental microbiome has recently been characterised and comprises non-pathogenic commensal microbiota from the Firmicutes,
                        Tenericutes, Proteobacteria, Bacteroides and Fusobacteria phyla, which do share some similarities of the oral microbiome.<sup>2,3</sup> During the
                        first week of life, a full term infant is largely colonised by bacteria from the Actinobacteria, Proteobacteria, Bacteroides and much
                        less by the Firmicutes. Firmicutes and Tenericutes, however, dominate the gut microbiota of a pre-term infant. It is understood that
                        the exposure to antenatal sources of commensal bacteria, varies by length of gestation. During the third trimester of pregnancy the
                        bacterial density in the placenta and also that of the breast milk significantly increases. Further, at this time the foetus begins to
                        swallow large amounts of amniotic fluid that harbours commensal bacteria (see Figure 1).<sup>1,2</sup>

                    </p>

                    <br>
                    <img class="img-responsive center-block" width="500px;" src="/img/practitioner-education/human-mammary-microbiota.png" title="Schematic representation of the acquisition and development of the human mammary microbiota.">
