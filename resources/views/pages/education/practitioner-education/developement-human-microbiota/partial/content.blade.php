                    <p>
                        Staphylococcus and Streptococcus are among the most abundant genera found in breast milk. Lactic acid bacteria in breast milk generates
                        an anaerobic environment in the neonate gut by consuming oxygen, promoting Bifidobacterium growth and later for several intestinal
                        strains after weaning. The milk microbiota is vital for the neonates immune and GIT maturation, without bacteria this simply does
                        not occur.<sup>4</sup> There are large differences in neonatal gut colonisation between breastfed and formula fed infants. It has
                        been demonstrated that term breast fed infants are dominated by Bifidobacteria species but decreased Enterobacteria species, whereas
                        formula fed infants demonstrate a more diverse array of bacteria including Escherichia coli, Clostridium, Bacteroides,
                        Prevotella and Lactobacillus species.<sup>2</sup>
                    </p>

                    <p>
                        <strong>Maternal Dysbiosis</strong>
                    </p>

                    <p>
                        Alterations in the GIT microbiome is reported in numerous conditions such as obesity, type 2 diabetes mellitus, inflammatory bowel
                        conditions, depression etc. For example overweight pregnant women demonstrate low numbers of Bifidobacterium and Bacteroides species
                        compared to normal weight pregnant women.<sup>5</sup> It is apparent that the maternal GIT microbiome provides the cue for foetal and neonate.
                        colonisation via the proposed bacterial translocation to breast milk, placenta and amniotic fluid. If the mother therefore has a
                        dysbiotic gut, it will impact on the type of bacteria available for colonisation of the infant. Research has shown that supplementing
                        women during their pregnancy with probiotics can lead to those selected species colonisation in the infants gut. Further studies have
                        shown that administering Lactobacillus rhamnosus to mothers 4 weeks before delivery and 3 weeks after delivery induced specific changes
                        in the transfer and initial establishment of Bifidobacteria in neonates compared to those that received placebo.<sup>5,6</sup> The administration
                        of probiotics during pregnancy and after birth is supported by numerous research papers, particularly with L. rhamnosus and various Bifidobacteruim
                        species, however the results are not consistent. Furthermore, the GIT microbiome in the neonate is responsible for re-establishing a TH1 to TH2
                        balance, with a stimulation of TH17 and TReg Cells involved in infant immune maturation.<sup>2</sup>
                    </p>

                    <br>
                    <p><strong>Clinical Summary:</strong></p>

                    <p><strong>Who Will Benefit:</strong></p>

                    <p>

                        <li>Pregnant and breast feeding mothers.</li>
                    <li>Women planning on becoming pregnant.</li>
                    <li>Women experiencing vaginosis.</li>
                    <li>Women with gastrointestinal dysbiosis and symptoms of gastrointestinal discomfort.</li>

                    </p>




