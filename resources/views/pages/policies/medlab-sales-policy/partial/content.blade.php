                    <p>
                        <strong>Medlab Sales Policy 2014</strong>
                    </p>

                    <p>
                        <strong>Section 1 - Guidelines for Online Supply</strong>
                    </p>

                    <p>
                        <strong>1 (a) (i)</strong> All Health Practitioners must seek approval from Medlab to sell any Medlab product online.
                        The sale of Medlab products online, must be controlled by a Qualified Healthcare Professional, at all times.
                    </p>

                    <p>
                        <strong>1 (a) (ii)</strong> Only current Medlab accounts holders, with a valid Qualified Healthcare Professional,
                        are permitted to sell Medlab products online. You will provide the url for your website to Medlab (www.medlab.co),
                        as part of your account details to be stored on file.
                    </p>

                    <p>
                        <strong>1 (a) (iii)</strong> The Qualified Healthcare Professional(s) associated with the website will be held responsible
                        for the sale of Medlab products online.
                    </p>

                    <p>
                        <strong>1 (a) (iv)</strong> Medlab account holders selling Medlab products in retail outlets are not permitted to sell
                        products online unless the sale of the brand is moderated by a Qualified Healthcare Professional at all times.
                    </p>

                    <p>
                        <strong>1 (b) (i)</strong> Medlab products may only be sold online if the customer is under the direct care of the Qualified
                        Healthcare Professional in charge of the site. This means that the practitioner will only supply those patients for which they
                        have conducted an appropriate consultation commensurate with their ethical requirements as set out by their professional association.
                    </p>

                    <p>
                        <strong>1 (b) (ii)</strong> The public section of the website must stipulate that the customer must be under the care of the Qualified
                        Healthcare Professional employed by your business in order to purchase Medlab products online. Specifically, the wording
                        <em>"Only patients who have completed a consultation with us, may have access to purchase Medlab products on this site".</em>
                    </p>

                    <p>
                        <strong>1 (b) (iii)</strong> No Promotional offers, or discounted pricing may be displayed on Medlab products in the public
                        online environment. This includes sections of your website that are open access to general public, as well as, social media,
                        such as Facebook, Twitter, Instagram and online blogs, forums, or any other visible public interface online. All pricing must
                        only be made available after secure login to the aforementioned website.
                    </p>

                    <p>
                        <strong>1 (b) (iv)</strong> Under no circumstances are Medlab products to be displayed, or resold on auction, or price comparison
                        websites such as eBay. Online sale of Medlab products is only permissible via websites owned and operated by qualified healthcare professionals
                        under the strict limitations set out within this policy. Medlab account holders who are found to be associated with retailing Medlab products in
                        this way will have their account cancelled immediately.
                    </p>

                    <p>
                        <strong>1 (c) (i)</strong> Medlab reserves the right to place a suspension on supply to any account that has not provided the contact details and
                        credentials of a Qualified Healthcare Professional.
                    </p>

                    <p>
                        <strong>1 (c) (ii)</strong> Medlab must always be aware of the current Qualified Healthcare Professional working with the business. This means
                        Medlab must be notified when a Qualified Healthcare Professional leaves the business and another is taken on board.
                    </p>

                    <p>
                        <strong>1 (c) (iii)</strong> All accounts who wish to sell Medlab online must have read, signed and returned a copy of the Medlab Terms &
                        Conditions, Medlab Sales Policy and Medlab Privacy Policy in order to be supplied, and continued to be supplied, Medlab products. Accounts without
                        a copy of these policies on file may not be supplied with Medlab products. In signing these policies, you are also agreeing to abide by any future
                        policy amendments.
                    </p>

                    <p>
                        &nbsp;
                    </p>

                    <p>
                        <strong>Section 2 - Guidelines for Dispensing</strong>
                    </p>

                    <p>
                        <strong>2 (a)</strong> Medlab products are practitioner-only and should be supplied to patients under the care of the Qualified Healthcare
                        Professional following a consultation. Medlab recommends that standards for consultations and patient records should be maintained according
                        to professional standards set out by the Ethics and Code of Practice (or similar rules of conduct) to which the individual Qualified Healthcare
                        Professional is ethically bound.
                    </p>

                    <p>
                        Healthcare professions that may apply for a practitioner account include:
                    </p>
                    <ul>
                        <li>Medical Practitioner/ Specialist</li>
                        <li>Naturopath</li>
                        <li>Nutritionist</li>
                        <li>Osteopathy</li>
                        <li>Pharmacist</li>
                        <li>Physiotherapist</li>
                        <li>Personal Trainers with post-grad scientific qualifications</li>
                        <li>Psychologist</li>
                        <li>Nurses</li>
                        <li>Chiropractor</li>
                    </ul>

                    <p>
                        <br>
                    </p>

                    <p>
                        <strong>2 (b) (i)</strong> A Qualified Health practitioner may discuss using the Patient Management Tool (PMT) on the Medlab website for
                        repeat prescriptions and individualised treatment plans. The format of the consultation undertaken is at the discretion of the Qualified
                        Healthcare Professional.
                    </p>

                    <p>
                        <strong>2 (c)</strong> Medlab reserves the right to follow up with the relevant professional body where an issue involves the Medlab brand,
                        products or reputation. Medlab reserves the right to terminate supply of products for any breach of this sales policy, unlawful activity or
                        breach of profession association policies.
                    </p>

                    <p>
                        &nbsp;
                    </p>

                    <p>
                        <strong>Section 3 - Brand Appearance, Pricing & Advertising</strong>
                    </p>

                    <p>
                        <strong>3 (a) (i) </strong>Only Medlab product images and product information approved by Medlab Marketing Manager may be displayed on your website.
                    </p>

                    <p>
                        <strong>3 (a) (ii)</strong> Medlab retains full control over our intellectual property, brand placement and advertising.
                    </p>

                    <p>
                        We reserve the right to request changes, or removal of brand representation, if it doesn't fit with our brand manifesto.
                    </p>

                    <p>
                        <strong>3 (a) (iii)</strong> Under no circumstances are images, text, or logos to be taken directly from any Medlab material (website, electronic,
                        printed matter, or other) unless written permission has been granted by the Medlab Marketing Manager.
                    </p>

                    <p>
                        <strong>3 (a) (iv)</strong> It is the responsibility of the Qualified Healthcare Professional in charge of the site, to ensure that Medlab images,
                        text or logos must be kept up-to-date at all times, including keeping both new and old product information or other brand information changes updated.
                    </p>

                    <p>
                        <strong>3 (b) (i)</strong> Medlab product and brand imagery, information and product details pre-approved by Medlab may be visible on the public section
                        of the website.
                    </p>

                    <p>
                        <strong>3 (b) (ii) </strong>No promotional offers, or discounted pricing may be displayed on Medlab products in the online environment. This includes
                        the public and private sections of your website. It also includes but is not limited to, social media, such as Facebook, Twitter and Instagram as well
                        as online blogs, forums, or any other visible public interface online.
                    </p>

                    <p>
                        <strong>3 (c) (i)</strong> Medlab products must not be advertised online or promoted using price as a vehicle. This includes special offers, discounts,
                        bundle deals or percentage knock-offs.
                    </p>

                    <p>
                       <strong>3 (c) (ii)</strong> Under no circumstances are Medlab products to be displayed, or resold on auction, or price comparison websites such as eBay.
                        Online sale of Medlab products is only permissible via websites owned and operated by qualified healthcare professionals under the strict limitations
                        set out within this policy. Medlab account holders who are found to be associated with retailing Medlab products in this way will have their account
                        cancelled immediately.
                    </p>

                    <p>
                        <strong>3 (c) (iii)</strong> As Medlab products are practitioner-only, and not for self-prescribing, they are not to be represented in consumer-faced
                        advertising online unless having received prior written consent from the Marketing Manager.
                    </p>

                    <p>
                        <strong>3 (d) </strong>Medlab imagery, text and logos visible on your site must be pre-approved by the Medlab Marketing Manager.
                    </p>

