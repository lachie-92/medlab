                    <p>
                        <strong>Section 4 - Breach & Complaint Handling</strong>
                    </p>

                    <p>
                        <strong>4 (a)</strong> Websites will be subject to periodic review in order to determine whether the Medlab guidelines are being adhered to,
                        ensuring that fair trade and ethics are met.
                    </p>

                    <p>
                        <strong>4 (b)</strong> If an account holder is in breach of any of the aforementioned guidelines, a letter of warning will be issued. If the website
                        owner fails to respond within the defined time frame, and take the appropriate action to address the issues identified, future supply of Medlab
                        products to that account may be suspended or permanently discontinued.
                    </p>

                    <p>
                        <strong>4 (c) </strong>If you believe that at any point the Medlab Sales Policy has been breached by an account holder, please contact Medlab
                        and request a "Breach Notification Form" to lodge your concern and allow appropriate action to be taken.
                    </p>

                    <p>
                        <strong>4 (d)</strong> Medlab believes it is the responsibility of the relevant professional associations to uphold the standards of conduct in
                        the health industry. As a commercial entity we do not hold the position to enforce Codes of Ethics of individual Qualified Healthcare Professionals.
                    </p>

                    <p>
                        <strong>4 (e)</strong> Medlab reserves the right to follow up with the relevant professional body where an issue involved the Medlab brand, products
                        or reputation.
                    </p>

                    <p>
                        &nbsp;
                    </p>

                    <p>
                        These guidelines serve only to cover the sale of the Medlab product range. If there is an issue regarding the sale of other practitioner products
                        by unqualified practitioners or unprofessional conduct related to the sale of practitioner-only products, Medlab advises that these matters should be
                        taken up with the relevant professional association. Please contact Medlab on 1300 369 570 if further clarification of modalities is required.
                    </p>

